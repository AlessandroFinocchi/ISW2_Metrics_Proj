package it.uniroma2.alessandro.exceptions;

public class IncorrectProportionException extends Exception{
    public IncorrectProportionException() {
        super();
    }

    public IncorrectProportionException(String message, Throwable cause) {
        super(message, cause);
    }

    public IncorrectProportionException(String message) {
        super(message);
    }
}
